package _4_controlStatements.looping.doWhileLoop;

public class Demo1 {
    public static void main(String[] args) {
        for (int i = 1; i > 10; i++) {
            System.out.println("for loop");
        }
        int i = 1;
        while (i > 10) {
            System.out.println("for loop");
            i++;
        }
        // 0 to n
    }
}
