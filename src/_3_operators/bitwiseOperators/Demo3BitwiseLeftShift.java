package _3_operators.bitwiseOperators;

public class Demo3BitwiseLeftShift {
    public static void main(String[] args) {
        System.out.println(4 << 1);//8 --> 4 * 2^1 ==> 8
        System.out.println(4 << 2);//16 --> 4 * 2 ^2 --> 16
        System.out.println(4 << 3);//32 --> 4 * 2 ^ 3--> 32
        System.out.println(4 << 4);//64 --> 4 * 2 ^ 3 --> 64
    }
}
