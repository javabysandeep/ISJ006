package _3_operators.arithmeticOperators;

public class Demo3 {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'a';
        System.out.println(ch1 + ch2);
        System.out.println(ch1 - ch2);
        System.out.println(ch1 * ch2);
        System.out.println(ch1 / ch2);
        System.out.println(ch1 % ch2);
        System.out.println('A' * 1.5);//


    }
}
