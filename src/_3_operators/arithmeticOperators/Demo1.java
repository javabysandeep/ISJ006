package _3_operators.arithmeticOperators;

public class Demo1 {
    public static void main(String[] args) {
        int number1 = 100;
        int number2 = 3;
        System.out.println(number1 + number2);//103
        System.out.println(number1 - number2);//97
        System.out.println(number1 * number2);//300
        System.out.println(number1 / number2);//33
        System.out.println(number1 % number2);//1
    }
}
